def get_field_cn(key, table):
    f = table.get('columns').get(key)
    if f is None:
        return key
    return f.get('cn')


def get_field_cns(cols, format_hints=None, indicator_keys=None):
    data = []
    for k in cols:
        # 根据列的实际类型确定数据类型标识
        col_type = cols[k].get('type')
        data_type = get_field_type_name(col_type) if col_type else 'string'
        # BIGINT 类型单独标识为 bigint，用于前端区分大数值和普通数值
        if col_type == BIGINT:
            data_type = 'bigint'

        entry = {"value": k, "caption": cols[k]['cn'], "width": cols[k]['size'],
                 "dataType": data_type,
                 "headerStyle": {"font": "bold 9pt Calibri", "wordWrap": "true"}}
        # 格式化元数据（从 FIELD_FORMAT_MAP 注入）
        if format_hints:
            fmt_info = format_hints.get(k, {})
            if 'fmt' in fmt_info:
                entry['format'] = fmt_info['fmt']
            if fmt_info.get('color'):
                entry['color'] = True
        # 指标列分组标记
        if indicator_keys and k in indicator_keys:
            entry['group'] = 'ind'
        if k == 'code':
            entry["style"] = ""
        elif k == 'change_rate':
            entry["conditionalFormats"] = [
                    {"ruleType": "formulaRule", "formula": "@>0", "style": {"foreColor": "red"}},
                    {"ruleType": "formulaRule", "formula": "@<0", "style": {"foreColor": "green"}}]
        data.append(entry)
    return data


def get_field_types(cols):
    data = {}
    for k in cols:
        data[k] = cols[k]['type']
    return data


def get_field_type_name(col_type):
    if col_type == DATE:
        return "datetime"
    elif col_type == FLOAT or col_type == BIGINT or col_type == SmallInteger or col_type == BIT:
        return "numeric"
    else:
        return "string"
